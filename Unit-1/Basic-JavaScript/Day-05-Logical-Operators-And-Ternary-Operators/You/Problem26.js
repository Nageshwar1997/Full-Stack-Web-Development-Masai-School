let weather = "Dark";

switch (weather) {
  case "Sunny":
    console.log("It is a beautiful day");
    break;
  case "Cloudy":
    console.log("It is bit cloudy but still pleasant");
    break;
  case "Rainy":
    console.log("It is a raining day");
    break;
  default:
    console.log("sorry, we do not have current weather information"); // sorry, we do not have current weather information
}