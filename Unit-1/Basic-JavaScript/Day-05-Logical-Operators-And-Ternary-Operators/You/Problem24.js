// NOT operator (!)

// NOT operator will simply convert true statement into false statement and false statement into true statement

// console.log(true);
console.log(!(true)); // false

// console.log(false);
console.log(!(false)); // true



let condition = true;
console.log(!(condition)); // false   (true into false)

let condition_ = false;
console.log(!(condition_)); // true   (false into true)


// console.log(5>4); // true
console.log(!(5 > 4)); // false

// console.log(1>4); // false
console.log(!(1 > 4)); // true

