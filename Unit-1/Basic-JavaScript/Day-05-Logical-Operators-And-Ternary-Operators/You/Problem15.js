// Ternary operators

// 1 condition

let dream_mobile = "iphone";

let electronic_store = "oneplus";

(dream_mobile === electronic_store) ? console.log("i will buy a phone") : console.log("i will not buy a phone");
//  i will not buy a phone


// more than 1 conditions

let a = 3;
let b = 3;

(a > b) ? console.log("a is greater") : (b > a) ? console.log("b is greater") : console.log("both a and b are equal");
// both a and b are equal