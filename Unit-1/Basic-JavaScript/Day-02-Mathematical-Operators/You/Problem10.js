// Concatenation

// number+number=number
let a = 5;
let b = 4;
console.log(a + b); // 9
console.log(typeof (a + b)); // number

// string+string=string
let c = "Hi";
let d = "Everyone";
console.log(c + d); // HiEveryone
console.log(typeof (c + d)); // string

// string + number = string
let e = "Hi";
let f = 5;
console.log(e + f); // Hi5
console.log(typeof (e + f)); // string