// Types of functions:-

// 1. Normal Function ----> ES5

function functionName(parameters){
  // return Code Logic;
  console.log("Normal Function")
}
functionName(); // calling a function



// 2. Anonymous Function ----> ES6

let variable_as_a_function_name=function (parameters){
  // return Code Logic;
  console.log("Anonymous Function");
}
variable_as_a_function_name();



// 3. Arrow Function or Anonymous Arrow Function ----> ES6

let variableAsAFunctionName= ()=>{
  // return Code logic;
  console.log("Arrow Function")
}
variableAsAFunctionName();