// Code 10 : toLowerCase() vs toUpperCase()
// toLowerCase()
// The toLowerCase() method is used to convert a string to lowercase letters. It returns a new string with all the alphabetic characters in the original string converted to their lowercase counterparts, while leaving non-alphabetic characters unchanged.

let sentence1 = 'The quIck bRown foX jumPs oVer tHe lAzy dOg.';

let result = sentence1.toLowerCase();
console.log(result);


// toUpperCase()
// The toUpperCase() method is used to convert all the characters in a string to uppercase. It operates on a string and returns a new string with all uppercase letters.

let sentence2 = 'The quIck bRown foX jumPs oVer tHe lAzy dOg.';
let result2 = sentence2.toUpperCase();
console.log(result2);
