let a = 5;
console.log(a); // 5

a++; // it will decrease the value by 1
console.log(a); // 6

a--; // it will decrease the value by 1
console.log(a); // 5

a++; // it will decrease the value by 1
console.log(a); // 6

--a; // it will decrease the value by 1
console.log(a); // 5

++a; // it will decrease the value by 1
console.log(a); // 6

--a; // it will decrease the value by 1
console.log(a); // 5
