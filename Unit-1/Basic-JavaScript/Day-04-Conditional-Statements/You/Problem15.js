// Given a char , you need to print whether the char is a vowel or not
// vowels : a, e, i, o, u


let char = "b"

if (char == "a") {
  console.log("vowel");
}
else if (char == "e") {
  console.log("vowel");
}
else if (char == "i") {
  console.log("vowel");
}
else if (char == "o") {
  console.log("vowel");
}
else if (char == "u") {
  console.log("vowel");
}
else {
  console.log("Not a vowel");
}